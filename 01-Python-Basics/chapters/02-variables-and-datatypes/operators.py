# Python Operators - Simple Example

# 1. Arithmetic & Assignment Operators
num = 3

num = num + 8
num = num * 6
num = num / 29

print("Value of num =", num)
print()

# 2. Comparison Operators
a = 10
b = 5

print("a == b :", a == b)
print("a != b :", a != b)
print("a > b  :", a > b)
print("a < b  :", a < b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)
print()

# 3. Logical Operators
print("True and False =", True and False)
print("True or False  =", True or False)
print("not True       =", not True)
